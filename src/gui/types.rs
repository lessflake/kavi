#[derive(Debug, Clone)]
pub struct KeyEvent {
    pub state: KeyState,
    pub key: Key,
    pub translated: Option<char>,
    pub mods: Modifiers,
    pub repeat: bool,
}

#[derive(Debug, Copy, Clone)]
pub enum KeyState {
    Press,
    Release,
}

bitflags::bitflags! {
    pub struct Modifiers: u8 {
        const CONTROL = 0x01;
        const ALT = 0x02;
        const SHIFT = 0x04;
    }
}

